static int iabs(int x){return x>0?x:-x;}
static void iswap(int*a,int*b){int t=*a;*a=*b;*b=t;}

// ── draw into back buffer ──────────────────────────────────────────────────
static void plot(int x,int y,short c){
    if((unsigned)x<320u&&(unsigned)y<240u) back_buf[y][x]=c;
}
static void fill_rect(int x,int y,int w,int h,short c){
    for(int r=0;r<h;r++) for(int cc=0;cc<w;cc++) plot(x+cc,y+r,c);
}
static void fill_circle(int cx,int cy,int r,short c){
    for(int dy=-r;dy<=r;dy++)
        for(int dx=-r;dx<=r;dx++)
            if(dx*dx+dy*dy<=r*r) plot(cx+dx,cy+dy,c);
}
static void outline_circle(int cx,int cy,int r,short c){
    int x=0,y=r,d=1-r;
    while(x<=y){
        plot(cx+x,cy+y,c);plot(cx-x,cy+y,c);
        plot(cx+x,cy-y,c);plot(cx-x,cy-y,c);
        plot(cx+y,cy+x,c);plot(cx-y,cy+x,c);
        plot(cx+y,cy-x,c);plot(cx-y,cy-x,c);
        if(d<0)d+=2*x+3; else{d+=2*(x-y)+5;y--;}
        x++;
    }
}
static void draw_line(int x0,int y0,int x1,int y1,short c){
    int steep=iabs(y1-y0)>iabs(x1-x0);
    if(steep){iswap(&x0,&y0);iswap(&x1,&y1);}
    if(x0>x1){iswap(&x0,&x1);iswap(&y0,&y1);}
    int dx=x1-x0,dy=iabs(y1-y0);
    int err=-(dx/2),y=y0,ys=y0<y1?1:-1;
    for(int i=x0;i<=x1;i++){
        if(steep)plot(y,i,c);else plot(i,y,c);
        err+=dy; if(err>0){y+=ys;err-=dx;}
    }
}
static void quad_fill(int x0,int y0,int x1,int y1,
                      int x2,int y2,int x3,int y3,short c){
    int xs[4]={x0,x1,x2,x3},ys[4]={y0,y1,y2,y3};
    int ymin=ys[0],ymax=ys[0];
    for(int i=1;i<4;i++){if(ys[i]<ymin)ymin=ys[i];if(ys[i]>ymax)ymax=ys[i];}
    for(int y=ymin;y<=ymax;y++){
        int its[4],cnt=0;
        for(int i=0;i<4;i++){
            int ax=xs[i],ay=ys[i],bx=xs[(i+1)%4],by=ys[(i+1)%4];
            if((y>ay&&y<=by)||(y>by&&y<=ay))
                its[cnt++]=ax+(y-ay)*(bx-ax)/(by-ay);
        }
        for(int a=0;a<cnt-1;a++)
            for(int b=a+1;b<cnt;b++)
                if(its[a]>its[b]){int t=its[a];its[a]=its[b];its[b]=t;}
        for(int a=0;a<cnt-1;a+=2)
            for(int x=its[a];x<=its[a+1];x++) plot(x,y,c);
    }
}

// ── 3D projection via camera orbit ────────────────────────────────────────
typedef struct{int x,y;}P2;

// Camera is at (cam_ox, cam_oy, 0); board at z=CAM_Z (-250).
// Translate world point relative to camera, then standard pinhole.
static P2 project(int wx,int wy,int wz){
    int rx=wx-cam_ox, ry=wy-cam_oy;
    // rz = wz - 0 = wz  (camera z = 0)
    // screen_x = -rx * FOCAL / (FOCAL - rz) + 160
    // denominator: FOCAL - wz;  wz is negative so denom > FOCAL → correct
    int denom = FOCAL - wz;
    P2 p;
    p.x = (-rx*FOCAL)/denom + 160;
    p.y = (-ry*FOCAL)/denom + 120;
    return p;
}

typedef struct{P2 ftl,ftr,fbl,fbr,btl,btr,bbl,bbr;}Box;

static Box get_box(int wx,int wy,int wz){
    Box b;
    b.ftl=project(wx-HW,wy+HW,wz+HW); b.ftr=project(wx+HW,wy+HW,wz+HW);
    b.fbl=project(wx-HW,wy-HW,wz+HW); b.fbr=project(wx+HW,wy-HW,wz+HW);
    b.btl=project(wx-HW,wy+HW,wz-HW); b.btr=project(wx+HW,wy+HW,wz-HW);
    b.bbl=project(wx-HW,wy-HW,wz-HW); b.bbr=project(wx+HW,wy-HW,wz-HW);
    return b;
}

static void draw_wall_tile(int col,int row){
    int wx=col*3*TILE-260;
    int wy=row*3*TILE-270;
    Box b=get_box(wx,wy,CAM_Z);

    // filled faces first so outlines overdraw cleanly
    quad_fill(b.ftl.x,b.ftl.y,b.ftr.x,b.ftr.y,
              b.fbr.x,b.fbr.y,b.fbl.x,b.fbl.y, COL_WALL_TOP);
    if(wy<0)
        quad_fill(b.btl.x,b.btl.y,b.btr.x,b.btr.y,
                  b.ftr.x,b.ftr.y,b.ftl.x,b.ftl.y, RED);
    if(wx>0)
        quad_fill(b.fbl.x,b.fbl.y,b.bbl.x,b.bbl.y,
                  b.btl.x,b.btl.y,b.ftl.x,b.ftl.y, COL_WALL_L);
    if(wx<0)
        quad_fill(b.ftr.x,b.ftr.y,b.btr.x,b.btr.y,
                  b.bbr.x,b.bbr.y,b.fbr.x,b.fbr.y, COL_WALL_R);
    if(wy>0)
        quad_fill(b.fbl.x,b.fbl.y,b.fbr.x,b.fbr.y,
                  b.bbr.x,b.bbr.y,b.bbl.x,b.bbl.y, COL_WALL_BOT);

    short e=COL_EDGE;
    draw_line(b.ftl.x,b.ftl.y,b.ftr.x,b.ftr.y,e);
    draw_line(b.ftr.x,b.ftr.y,b.fbr.x,b.fbr.y,e);
    draw_line(b.fbr.x,b.fbr.y,b.fbl.x,b.fbl.y,e);
    draw_line(b.fbl.x,b.fbl.y,b.ftl.x,b.ftl.y,e);
    if(wy<0)draw_line(b.btl.x,b.btl.y,b.btr.x,b.btr.y,e);
    if(wx<0)draw_line(b.btr.x,b.btr.y,b.bbr.x,b.bbr.y,e);
    if(wy>0)draw_line(b.bbr.x,b.bbr.y,b.bbl.x,b.bbl.y,e);
    if(wx>0)draw_line(b.bbl.x,b.bbl.y,b.btl.x,b.btl.y,e);
    if((wy<0)||(wx>0))draw_line(b.ftl.x,b.ftl.y,b.btl.x,b.btl.y,e);
    if((wy<0)||(wx<0))draw_line(b.ftr.x,b.ftr.y,b.btr.x,b.btr.y,e);
    if((wy>0)||(wx>0))draw_line(b.fbl.x,b.fbl.y,b.bbl.x,b.bbl.y,e);
    if((wy>0)||(wx<0))draw_line(b.fbr.x,b.fbr.y,b.bbr.x,b.bbr.y,e);
}

