// ── full composite into back buffer ───────────────────────────────────────
static void redraw_all(int m,int px,int py){
    // clear
    for(int y=0;y<240;y++)
        for(int x=0;x<320;x++)
            back_buf[y][x]=BLACK;
    // walls
    for(int r=0;r<ROWS;r++)
        for(int c=0;c<COLS;c++)
            if(maps[m][r][c]==1)
                draw_wall_tile(c,r);
    // sprites
    draw_target();
    draw_ball(agent_px,agent_py,1);
    draw_ball(px,py,0);
}

// ── vsync flip ─────────────────────────────────────────────────────────────
// Tells the hardware to start displaying what is currently in back_buf,
// waits for vsync, then swaps pointers so back_buf points to the old front.
static void flip(void){
    volatile int *ctrl=(int*)0xFF203020;
    // write address of what we just drew (back_buf) as the new front
    *(ctrl+1)=(int)back_buf;
    *ctrl=1;                          // trigger swap request
    while((*(ctrl+3)&1)!=0);         // wait for hardware to latch
    // swap CPU-side pointers
    short int (*tmp)[512]=front_buf;
    front_buf=back_buf;
    back_buf=tmp;
    // point register at the new back buffer so HW knows where we will write next
    *(ctrl+1)=(int)back_buf;
}

// ── hardware ──────────────────────────────────────────────────────────────
volatile int *ps2=(volatile int*)0xFF200100;
static int ps2_read(void){int v=*ps2;if(v&0x8000)return v&0xFF;return -1;}

// ── collision ──────────────────────────────────────────────────────────────
static int hits_wall(int m,int bx,int by){
    int pts[4][2]={{bx,by},{bx+BALL_SIZE-1,by},
                   {bx,by+BALL_SIZE-1},{bx+BALL_SIZE-1,by+BALL_SIZE-1}};
    for(int i=0;i<4;i++){
        int c=(pts[i][0]-MAP_OFFSET_X)/TILE;
        int r=(pts[i][1]-MAP_OFFSET_Y)/TILE;
        if(c<0||c>=COLS||r<0||r>=ROWS)return 1;
        if(maps[m][r][c]==1)return 1;
    }
    return 0;
}
static int reached_target(int bx,int by){
    int bc=(bx+BALL_SIZE/2-MAP_OFFSET_X)/TILE;
    int br=(by+BALL_SIZE/2-MAP_OFFSET_Y)/TILE;
    return(bc==target_col&&br==target_row);
}

// ── spawn ──────────────────────────────────────────────────────────────────
static void spawn_target(int m,int bc,int br){
    int col,row;
    do{col=1+rand()%(COLS-2);row=1+rand()%(ROWS-2);}
    while(maps[m][row][col]!=0||(col==bc&&row==br));
    target_col=col; target_row=row;
}
static void reset_round(int *cm,int *px,int *py){
    *cm=rand()%NUM_MAPS;
    *px=col_to_px(1); *py=row_to_py(1);
    agent_px=col_to_px(2); agent_py=row_to_py(1);
    pulse_phase=0;
    spawn_target(*cm,1,1);
}

// ── main ───────────────────────────────────────────────────────────────────
int main(void){
    volatile int *pixel_ctrl=(int*)0xFF203020;

    // Bootstrap double buffer:
    // Buffer1 = initial back (we draw into this first)
    // Buffer2 = initial front (hardware displays this — starts as garbage/black)
    front_buf=Buffer2;
    back_buf =Buffer1;

    // Init hardware to show Buffer2 as front
    *(pixel_ctrl+1)=(int)Buffer2;
    *pixel_ctrl=1;
    while((*(pixel_ctrl+3)&1)!=0);

    int cm=0;
    int px=col_to_px(1), py=row_to_py(1);
    agent_px=col_to_px(2); agent_py=row_to_py(1);
    pulse_phase=0; cam_ox=0; cam_oy=0; osc_phase=0;
    spawn_target(cm,1,1);

    // Draw initial frame and flip
    redraw_all(cm,px,py);
    flip();

    int e0=0,skip=0;
    int agent_tick=0;
    int needs_redraw=0;

    while(1){

        // ── agent tick: move + advance osc + advance pulse ─────────────────
        agent_tick++;
        if(agent_tick >= 80000){
            agent_tick=0;

            // advance pulse ring
            pulse_phase=(pulse_phase+1)&3;

            // advance camera orbit — one step per agent move
            // Using two sinusoids 90° apart gives a smooth elliptical tilt
            osc_phase=(osc_phase+1)&63;
            cam_ox=(sin64[osc_phase]          * TILT_R) / 32;
            cam_oy=(sin64[(osc_phase+16)&63]  * TILT_R) / 32;

            // snap agent to tile, look up Q-table, move
            int ac=px_to_col(agent_px), ar=py_to_row(agent_py);
            agent_px=col_to_px(ac); agent_py=row_to_py(ar);

            int action=qt_best_action(ac,ar,target_col,target_row,cm);
            int nac=ac,nar=ar;
            if     (action==0)nar--;
            else if(action==1)nar++;
            else if(action==2)nac--;
            else if(action==3)nac++;
            if(nac>=0&&nac<COLS&&nar>=0&&nar<ROWS&&maps[cm][nar][nac]==0){
                agent_px=col_to_px(nac); agent_py=row_to_py(nar);
            }

            if(reached_target(agent_px,agent_py))
                reset_round(&cm,&px,&py);

            needs_redraw=1;
        }

        // ── player input ───────────────────────────────────────────────────
        int b;
        while((b=ps2_read())>=0){
            if(skip){skip=0;e0=0;continue;}
            if(b==0xF0){skip=1;continue;}
            if(b==0xE0){e0=1;continue;}

            int nx=px,ny=py;
            if(e0){
                if     (b==0x6B)nx-=SPEED;
                else if(b==0x74)nx+=SPEED;
                else if(b==0x75)ny-=SPEED;
                else if(b==0x72)ny+=SPEED;
                e0=0;
            } else {
                if     (b==0x1D)ny-=SPEED;
                else if(b==0x1B)ny+=SPEED;
                else if(b==0x1C)nx-=SPEED;
                else if(b==0x23)nx+=SPEED;
            }
            if(!hits_wall(cm,nx,ny)){px=nx;py=ny;}
            if(reached_target(px,py))
                reset_round(&cm,&px,&py);
            needs_redraw=1;
        }

        // ── composite and flip once per dirty frame ────────────────────────
        if(needs_redraw){
            needs_redraw=0;
            redraw_all(cm,px,py);
            flip();
        }
    }
    return 0;
}