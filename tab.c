// ball.c  —  Polished Edition v2
//
// KEY FIXES vs v1:
//   • No flicker: proper double-buffering. We always draw into the BACK buffer
//     and flip with wait_for_vsync(). The full scene is composited off-screen
//     before it ever reaches the display — zero tearing, zero flash.
//
//   • True perspective tilt: the camera XY position slowly orbits the board
//     centre via integer sin/cos. Moving the camera (not the world) means the
//     vanishing point shifts — walls lean convincingly as if the board is
//     being physically tilted. Radius = TILT_R world units (kept subtle).
//
//   • Tilt advances once per agent move (not on a separate timer) so there is
//     no extra redraw cost and the motion stays perfectly in sync with gameplay.
//
//   • Target: 4-frame outward-ripple single blue ring over a solid cyan disc.
//   • Balls:  layered fill_circle with specular highlight, no ghost pixels.
//   • Walls:  quad_fill for every visible face, then dark outline on top.
//
// Requires: q_table.h  (auto-generated by train.py)
// Hardware: DE1-SoC  320x240 VGA, PS2 keyboard

